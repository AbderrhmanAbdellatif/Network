    void showGame() {
        String Temp = "";
        Temp += ("\n");
        Temp += "\n " + "-----------------------------" + " \n";
        Temp += " \n" + "|    Connect 4  Game    |" + "\n";
        Temp += "\n" + "------------------------------" + "\n";
        for (int i = 0; i < gUI.board.length; i++) {//satirlar icin 
            for (int j = 0; j < gUI.board.length; j++) {//kolumlar icin
                if (gUI.board[i][j] == null) {
                    Temp += "  \t|   " + " ";
                } else {
                    Temp += (" | " + gUI.board[i][j] + " ");
                }
            }
            Temp += ("|");
            Temp += ("\n");
        }
        Temp += ("\n----------------------------- \n ");
        Temp += ("\n | 1   2   3   4   5   6 | \n ");
        Temp += ("\n ----------------------------- \n");

        //showArea.setText(Temp);
    }

